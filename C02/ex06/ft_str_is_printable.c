int	ft_str_is_printable(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0') // while the string has not terminated
	{
		if ((str[i] >= 32) && (str[i] <= 126))
			i++;
		else
			return (0);
	}
	return (1);
}
